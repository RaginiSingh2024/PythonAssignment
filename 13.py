## String Slicing
my_string = "Hello, World!"

# Reverse the string
reversed_string = my_string[::-1] 
print("Reversed string:", reversed_string)

# Skip every other letter
skipped_string = my_string[::2] 
print("String with every other letter skipped:", skipped_string)

# Extract a substring
substring = my_string[7:12]  # Extract "World"
print("Substring:", substring)